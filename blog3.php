              <h2>
                New Joint Venture of Agrico and S.V. Agri to Offer High
                Quality Seed Sotatoes in India
              </h2>
            </div>
            <img src="./assets/images/blog/blog3.jpg" alt="A row of green potato plants growing in the dirt." />
            <p class="para-news-credit">www.potatopro.com</p>
            <p class="text-justify">
              Agrico, a world-renowned Dutch cooperative of more than 1,300
              specialist potato growers, has agreed to set up a joint venture
              with Pune-based potato supply chain organization S.V. Agri to
              develop high-yield varieties uniquely suitable to India.
            </p>

            <p class="text-justify">
              The new joint venture, named S.V. Agrico Pvt. Ltd., will produce
              high-grade basic seed material under controlled conditions.
              Agrico varieties, which are widely accepted across the world,
              are resistant to late blight. Their availability locally will
              help Indian farmers reduce usage of fungicide, allowing them to
              access more remunerative international markets for organic
              potatoes.
            </p>
            <p class="text-justify">
              Potatoes are a superfood with enough nutrition to feed a growing
              world population. Since India does not allow imports of
              soil-grown seed potatoes from other countries, local
              multiplication under license is the only option for the
              country’s farmers to enjoy a potato revolution: higher yields
              and better prices.
            </p>
            <p class="text-justify">
              The joint venture will provide the necessary infrastructure to
              bring Agrico varieties to the Indian market in a dedicated
              supply chain and based on local multiplication only.
            </p>
            <p class="text-justify">
              The agreement, which was signed by Hemant Gaur and Jan van
              Hoogen, marks the culmination of a successful, seven-year
              partnership. Agrico and S.V. Agri started their technical
              co-operation in 2012.
            </p>
            <p class="text-justify">
              Going forward, field multiplication and trade in seed potatoes
              developed by the joint venture will remain an exclusive right of
              S.V. Agri, the Indian partner.
            </p>
            <p class="block-quote quote-1 text-justify">
              S.V. Agri will also learn world-class quality management of the
              seeds as well as objective quality assessment of the warehouse
              potato.
            </p>
            <p class="block-quote quote-1 text-justify">
              Over 400 years, Holland has perfected its potato technology. We
              want to bring benefit of that knowledge to our farmers.
              <span class="quote-author heading-green">- Hemant Gaur</span>
            </p>
            <p class="block-quote quote-1 text-justify">
              Coming to India is a big step in building up the international
              Agrico network.
            </p>
            <p class="block-quote quote-1 text-justify">
              The ‘pieper’ — as we Dutch call it — is so much a part of our
              culture that one of our artists, Vincent Van Gogh, immortalized
              it in his masterpiece, The Potato Eaters.
              <span class="quote-author heading-green">- Jan Van Hoogen</span>
            </p>
