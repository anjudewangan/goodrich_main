              <h2>New Book to Guide Potato Research and Development</h2>
            </div>
            <img src="./assets/images/blog/blog2.jpg" alt="A row of green potato plants growing in the dirt." />
            <p class="para-news-credit"> www.potatopro.com</p>
            <p class="text-justify">
              Few people are aware that the potato is the third most important
              global crop in terms of human consumption. It contributes to the
              food and nutrition security, climate resilience and incomes of
              millions of resource-poor families across the Global South
            </p>

            <p class="text-justify">
              However, achieving the potato’s full potential to feed a growing
              population in a climate-changing world requires research, new
              technologies and other innovations.
            </p>
            <p class="text-justify">
              To contribute to that process and share information on some of
              the latest advances in potato science, two International Potato
              Center (CIP) scientists edited an extensive compilation of
              research and expert perspectives in the book: 'The potato crop:
              its agricultural, nutritional and social contribution to
              humankind,' recently published by the prestigious publishing
              house Springer Nature.
            </p>
            <p class="text-justify">
              The work of over 60 scientists from 25 countries and 23
              international research centers, universities, government
              agencies and industry, the book brings together an array of
              useful, research-based information on areas ranging from
              potato’s nutritional contribution to genetics and genetic
              resources to value chains.
            </p>
            <hr class="space m" />
            <p class="block-quote quote-2 text-justify">
              This book reflects the evolution, state of the art and
              challenges of potato research for development, in both the
              biophysical and socioeconomic sciences.
            </p>
            <p class="block-quote quote-2 text-justify">
              It contains a wealth of information that can help scientists,
              extension workers and potato farmers better understand the
              current state of potato science and where it is likely to go in
              the near future.
              <span class="quote-author heading-green">- Oscar Ortiz, CIP Deputy Director General for Research and
                Development and co-editor of ‘The Potato Crop’</span>
            </p>
            <hr class="space m" />
            <p class="text-justify">
              The book consists of 14 chapters organized into four sections
              covering themes that include the potato’s contribution to global
              food security, diets, health and incomes; advances in potato
              biodiversity conservation, breeding and genetics; agronomic
              concerns such as crop pests, diseases and seed systems; and
              participatory research and gender considerations. Much of the
              book addresses needs and challenges in developing countries,
              where potato farming and consumption are on the rise, and where
              there are opportunities to significantly improve potato yields
              and benefits for resource-poor households.
            </p>
            <p class="block-quote quote-1 text-justify">
              The goal is to make this large and updated body of knowledge
              freely available to the widest, largest possible audience,
              including researchers, students, international development
              professionals and policy makers.
            </p>
            <p class="block-quote quote-1 text-justify">
              My hope is that this book will inspire more young men and women
              scientists to focus on the potato and contribute to harnessing
              its potential to improve lives and help farmers in developing
              countries adapt to climate change.
              <span class="quote-author heading-green">- Hugo Campos, CIP Research Director and co-editor of the book</span>
            </p>
            <hr class="space m" />
            <p class="text-justify">
              He explained that the book has consequently been published as
              Open Access, thanks to support from the CGIAR Research Program
              on Roots, Tubers and Bananas. It is thus fully available, free
              of charge, to anyone interested in this fascinating crop.
            </p>
