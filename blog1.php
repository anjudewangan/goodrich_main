              <h2>Rwanda Pilots ‘Revolutionary’ Potato Seeds</h2>
            </div>
            <img src="./assets/images/blog/blog1.jpg" alt="Potatoes in a sack with a white flower, representing a bountiful harvest of freshly harvested produce." />
            <p class="para-news-credit"> www.potatopro.com</p>
            <p class="text-justify">
              The Rwanda Agriculture Board (RAB) has started testing True
              Potato Seeds, which are produced by Solynta, a Dutch firm that
              develops hybrid potatoes. True Potato Seeds are collected from
              the berries or fruits of the potato plant.
            </p>
            <p class="text-justify">
              One of the benefits of such seeds is that as opposed to using
              2,500 kilogrammes or 2.5 tonnes of perishable potato-tubers per
              one hectare, farmers will be able to plant only 25 grammes of
              seeds on the same size of land.
            </p>
            <p class="text-justify">
              If adopted, the new Irish potato breeds will reduce the cost
              that farmers incur and increase their access to seeds. Last
              month Solynta and RAB bolstered their collaboration in a bid to
              increase farmers’ access to hybrid potato seeds.
            </p>
            <p class="text-justify">
              Through their collaboration, they seek to supply a combination
              of True Potato Seeds and hybrids to local farmers.
            </p>
            <p class="text-justify">
              During every growing season, Rukundo said, potato is grown on
              between 50,000 and 60,000 hectares in Rwanda. Irish potato
              production in the country is estimated at 916,000 tonnes every
              year.
            </p>
            <p class="text-justify">
              Irish potato is the third staple food crop in Rwanda after
              Cassava and Sweet potato based on the volume of production. In
              addition, it is also an important cash crop in the North-Western
              parts of Rwanda.
            </p>
            <p class="text-justify">
              True Potato Seed, officials say, are resistant to diseases, easy
              to transport and store. Solynta says that the technology makes
              it possible to commercialise new varieties in just two years
              instead of seven years [compared to potato tubers].
            </p>
            <hr class="space m" />
            <p class="block-quote quote-1 text-justify">
              Collaboration is critical to addressing global challenges to economic and food security.
              <span class="quote-author heading-green">- Hein Kruyt, CEO of Solynta</span>
            </p>
            <p class="block-quote quote-1 text-justify">
              This is a revolutionary innovation for potato seeds.
            </p>
            <p class="block-quote quote-1 text-justify">
              We are excited to be on the forefront of this game-changing innovation and look forward to continuing to
              work together and build a stronger future for Rwanda and the world.
              <span class="quote-author heading-green">- RAB Director-General, Patrick Karangwa</span>
            </p>
            <p class="block-quote quote-1 text-justify">
              This technology is still under evaluation at [RAB research] station level, not yet at farm level.
            </p>
            <p class="block-quote quote-1 text-justify">
              This research started in [farming] season 2019 C. If we find that these true seeds are promising, we will
              move to another step: on-farm trial.
              <span class="quote-author heading-green">- Placide Rukundo, the Potato Programme Leader at RAB</span>
            </p>
